if (day == "mon") {
  console.log("Monday");
}
else if (day == "tue") {
  console.log("Tuesday");
}
else if (day == "wed") {
  console.log("Wednesday");
}
else if (day == "thu") {
  console.log("Thrusday");
}
else if (day == "fri") {
  console.log("Fridady");
}
else if (day == "sat") {
  console.log("Saturday");
}
